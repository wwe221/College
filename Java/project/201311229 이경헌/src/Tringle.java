public class Tringle {
	private double mit;
	private double high;
	
	public Tringle(double mitt, double highh)
	{
	mit=mitt;
	high=highh;
	}
	
	public double gethigh()
	{
	return high;
	}
	
	public double getmit()
	{
	return mit;
	}
	
	public double sethigh(double highh)
	{
		high=highh;
		return highh;
	}
	
	public double setmit(double mitt)
	{
	mit=mitt;
	return mit;
	}
	
	public double getarea()
	{
	return (mit*high)/2;
	}

}